package OOPSConcept;

public class SuperKeywords {
	
	public SuperKeywords() {
		System.out.println("Parent class constructor");
	}

	public SuperKeywords(int i) {
		System.out.println("Parent class constructor of i: " +i);
		
	}
	
	public SuperKeywords(String i, String j) {
		System.out.println("Parent class constructor of i: " +i);
		System.out.println("Parent class constructor of j: " +j);
		
	}
}
