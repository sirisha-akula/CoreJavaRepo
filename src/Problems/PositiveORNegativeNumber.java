package Problems;

public class PositiveORNegativeNumber {

	public static void main(String[] args) {
		double d = 43;
		if(d > 0.0) {
			System.out.println(d + " is positive number");
		} 
		
		else if(d < 0.0) {
			System.out.println(d + " is Negative number");
		} 
		
		else  {
			System.out.println(d + "is a zero number");
		}
		

	}

}
